#include <iostream> // Подключаем библиотеку для работы с вводом-выводом

using namespace std; // Используем пространство имен std

// Функция сортировки вставками
void insertionSort(int arr[], int n) {
    // Перебираем элементы массива, начиная со второго (индекса 1)
    for (int i = 1; i < n; i++) {
        int currentValue = arr[i]; // Сохраняем текущий элемент
        int position = i;          // Позиция текущего элемента
        
        // Сравниваем текущий элемент с предыдущими элементами массива
        // Двигаемся назад по уже отсортированной части массива
        while (position > 0 && arr[position - 1] > currentValue) {
            // Сдвигаем большие элементы вперед
            arr[position] = arr[position - 1];
            position--; // Уменьшаем позицию
        }
        
        // Вставляем текущий элемент в правильную позицию
        arr[position] = currentValue;
    }
}

// Функция для вывода массива
void printArray(int arr[], int n) {
    for (int i = 0; i < n; i++) {
        cout << arr[i] << " "; // Выводим каждый элемент массива
    }
    cout << endl; // Новый ряд после вывода массива
}

// Главная функция
int main() {
    int data[] = {12, 11, 13, 5, 6}; // Исходный массив
    int n = sizeof(data) / sizeof(data[0]);      // Вычисляем размер массива
    
    cout << "Исходный массив: ";
    printArray(data, n);                         // Выводим исходный массив
    
    insertionSort(data, n);                      // Сортируем массив
    
    cout << "Отсортированный массив: ";
    printArray(data, n);                         // Выводим отсортированный массив
    
    return 0;                                    // Завершение программы
}