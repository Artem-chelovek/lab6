def insertion_sort(arr):
    """
    Реализует алгоритм сортировки вставками.
    Алгоритм берет каждый последующий элемент и вставляет его в правильную позицию в уже отсортированном участке массива.
    """
    # Проходим по каждому элементу массива, начиная со второго (индекс 1)
    for i in range(1, len(arr)):
        key = arr[i]           # Сохраняем текущий элемент в отдельную переменную key
        j = i - 1              # Начнем сравнивать с предыдущим элементом
        
        # Двигаемся назад по уже отсортированным элементам
        while j >= 0 and key < arr[j]:  # Пока ключ меньше предыдущего элемента
            arr[j + 1] = arr[j]       # Смещаем больший элемент вправо
            j -= 1                     # Продолжаем двигаться назад
            
        # Когда найдена правильная позиция для ключа, вставляем его туда
        arr[j + 1] = key

# Пример использования
if __name__ == "__main__":
    example_array = [12, 11, 13, 5, 6]
    print("Исходный массив:", example_array)
    
    insertion_sort(example_array)  # Сортировка массива
    
    print("Отсортированный массив:", example_array)
    '''
    Исходный массив: [12, 11, 13, 5, 6]
    Отсортированный массив: [5, 6, 11, 12, 13]
    '''